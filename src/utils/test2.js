function getUniqueElements(arr1, arr2) {
    const set1 = new Set(arr1);
    const set2 = new Set(arr2);

    const uniqueArr1 = arr1.filter(item => !set2.has(item));
    const uniqueArr2 = arr2.filter(item => !set1.has(item));

    return [...uniqueArr1, ...uniqueArr2];
};

function removeDirectoryRecursive(folderPath) {
    if (fs.existsSync(folderPath)) {
        fs.readdirSync(folderPath).forEach((file) => {
            const currentPath = path.join(folderPath, file);
            if (fs.lstatSync(currentPath).isDirectory()) {
                removeDirectoryRecursive(currentPath); // Đệ quy nếu là thư mục con
            } else {
                fs.unlinkSync(currentPath); // Xóa tệp
            }
        });
        fs.rmdirSync(folderPath); // Xóa thư mục sau khi đã xóa hết nội dung
    }
}

